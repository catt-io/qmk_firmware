bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    switch(keycode) {
      case CK_RARW:
        SEND_STRING("->");
        return false;
      case CK_RFRW:
        SEND_STRING("=>");
        return false;
    }
  }
  return true;
};

